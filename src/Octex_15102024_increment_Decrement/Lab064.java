package Octex_15102024_increment_Decrement;

public class Lab064
{
    public static void main(String[] args) {
        int pre_a=10;  //1st increment then print
        System.out.println(++pre_a);
        System.out.println(pre_a);

        int post_a=10;//print 1st then increment
        System.out.println(post_a++);
        System.out.println(post_a);

    }
}
