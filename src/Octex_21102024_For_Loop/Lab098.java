package Octex_21102024_For_Loop;

public class Lab098
{
    public static void main(String[] args) {
        // A is demanding to find the even no from 1 to 15

        for(int i=0;i<=50;i++)
        {
            if (i%2==0)
            {
                System.out.println("no is even"+i);
            }
            else
            {
                System.out.println("odd no"+i);
            }
        }
    }
}
