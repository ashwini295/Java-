package Octex_21102024_For_Loop;

public class Lab092
{
    public static void main(String[] args) {
        for(float f=0.0f;f<10.67;f++)
        {
            System.out.println(f);
        }

        for(long l=10;l<100;l++)
        {
            System.out.println(l);
        }
        for (char c ='a'; c <='z'; c++ )
        {
            System.out.println(c);
        }
    }
}
