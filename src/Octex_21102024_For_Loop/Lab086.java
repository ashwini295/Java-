package Octex_21102024_For_Loop;

public class Lab086 {
    public static void main(String[] args) {
        for(int ashwini=0;ashwini<10;ashwini++)
        {
            System.out.println(ashwini);
            //ashwini-> 0,9->10 times
        }

        for(int ashwini=1;ashwini<=10;ashwini++)
        {
            System.out.println(ashwini);
            //ashwini-> 1,10->10 times
        }




    }
}
